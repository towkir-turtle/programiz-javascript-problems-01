// Example 1: JavaScript Program To Print Hello World
console.log("Hello, World!");

// Example 2: JavaScript Program to Add Two Numbers
const num1 = 34;
const num2 = 18;
const sum = num1 + num2;
console.log(`The sum of ${num1} and ${num2} is: ${sum}`);

// Example 3: JavaScript Program to Find the Square Root
const number = 25;
const result = Math.sqrt(number);
console.log(`The square root of ${number} is: ${result}`);

// Example 4: JavaScript Program to Calculate the Area of a Triangle
const baseValue = 5;
const heightValue = 7;
const areaValue = (baseValue * heightValue) / 2;
console.log(`The area of the triangle is: ${areaValue}`);

// Example 5: JavaScript Program to Swap Two Variables
let a = 15;
let b = 40;

let temp;

temp = a;
a = b;
b = temp;

console.log(`The value of a after swap is: ${a}`);
console.log(`The value of b after swap is: ${b}`);
